/**
 * \file groups.hpp
 * Documentation groups for the Opium library
 * 
 * This file contains only documentation and defines the main module groups
 * for organizing the API documentation.
 */

/**
 * \defgroup memory Memory Management
 * Memory allocation and management utilities
 * 
 * This group contains classes and functions for memory management,
 * including garbage collection and allocators.
 */

/**
 * \defgroup core Core Components
 * Core components of the Opium library
 * 
 * This group contains the fundamental classes and functions that form
 * the core of the Opium library.
 */

/**
 * \defgroup prolog Prolog Implementation
 * Prolog language implementation
 * 
 * This group contains classes and functions related to the Prolog
 * language implementation.
 */

/**
 * \defgroup lisp Lisp Implementation
 * Lisp language implementation
 * 
 * This group contains classes and functions related to the Lisp
 * language implementation, including parsers, readers, and code transformers.
 */

/**
 * \defgroup utils Utilities
 * Utility classes and functions
 * 
 * This group contains various utility classes and functions used
 * throughout the library.
 */

/**
 * \defgroup containers Container Adapters
 * STL container adapters
 * 
 * This group contains adapters for STL containers that work with
 * the Opium memory management system.
 */
